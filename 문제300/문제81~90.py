# 문제 87 # 다음 딕셔너리를 사용하여 메로나 가격을 출력하라.
아이스크림 = {'메로나': 1000,
         '폴라포': 1200,
         '빵빠레': 1800,
         '죠스바': 1200,
         '월드콘': 1500}
print(" 메로나 가격 : " , 아이스크림["메로나"])
    # 딕셔너리에 값을 출력 방법 : 딕셔너리명[ 키 ]   => 해당하는 키의 값이 출력 된다

# 문제 88 # 다음 딕셔너리에서 메로나의 가격을 1300으로 수정하라.
아이스크림 = { '메로나': 1000,
         '폴라포': 1200,
         '빵빠레': 1800,
         '죠스바': 1200,
         '월드콘': 1500 }

아이스크림["메로나"] = 1300 # 리스트와 딕셔너리는 수정 가능
print(" 메로나 가격 : " , 아이스크림["메로나"])

