# 문제 81 # 별 표현식 # star expression #  좌측 8개의 값을 valid_score 변수에 바인딩하여
scores = [8.8 , 8.9 , 8.7 , 9.2 , 9.3 , 9.7 , 9.9 , 9.5 , 7.8 , 9.4]
print( *scores ) # 리스트 제거
*scores,_,_ = scores # 뒤에서 2개 생략
print( scores )

# 문제 82  #  우측 8개의 값을 valid_score 변수에 바인딩하여라
scores = [8.8 , 8.9 , 8.7 , 9.2 , 9.3 , 9.7 , 9.9 , 9.5 , 7.8 , 9.4]
_,_,*scores = scores # 앞에서 2개 생략
print( scores )

# 문제83  # 가운데 8개만 valid_score 변수에 바인딩하여라
scores = [8.8 , 8.9 , 8.7 , 9.2 , 9.3 , 9.7 , 9.9 , 9.5 , 7.8 , 9.4]
_,*scores,_ = scores # 앞뒤로 1개 생략
print( scores )

